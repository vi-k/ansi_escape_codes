/// Bell.
const String bel = '\x07';

/// Backspace (\b).
const String backspace = '\x08';

/// Tabulation (\t).
const String tab = '\x09';

/// Line feed (\n).
const String lf = '\x0A';

/// Form feed (\f).
const String ff = '\x0C';

/// Carriage return (\r).
const String cr = '\x0D';

/// Escape.
const String esc = '\x1B';

/// OSC (Operating System Command).
const String osc = '$esc]';

/// ST (String Terminator).
const String st = '$esc\\';
